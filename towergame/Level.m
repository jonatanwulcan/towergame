@implementation Level

- (id) initWithLevelNumber:(int) levelNumber {
    self = [super init];
    return self;
}

- (bool) hasFloorWithX:(float) fx y:(float) fy {
    int x=floor(fx/32.0);
    int y=floor(fy/32.0);
    
    int ldx = x+10;
    int ldy = 50-y;
    return levelData[ldy*20+ldx] == 1;
}

- (void) draw {
    float cx = (((int)cameraX)/32)*32-16;
    float cy = (((int)cameraY)/32)*32-16;
    float w = 32*10;
    float h = 32*15;
    float minX = cx-w;
    float minY = cy-h;
    float maxX = cx+w;
    float maxY = cy+h;
    
    for(int x=minX;x<=maxX;x+=32) {
        for(int y=minY;y<=maxY;y+=32) {
            if([self hasFloorWithX:x y:y]) {
                [sprites[SPRITE_FLOOR] drawWithX:x y:y];
            }
        }
    }
}

@end
